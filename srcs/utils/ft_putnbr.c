#include <utils.h>

void	ft_putnbr(int fd, int n)
{
	if (n == -2147483648)
		write(2, "-2147483648", 11);
	if (n < 0 && n > -2147483648)
	{
		write(2, "-", 1);
		n *= -1;
	}
	if (n < 10 && n > -2147483648)
		write(2, &NUM[n], 1);
	else if (n >= 10)
	{
		ft_putnbr(fd, n / 10);
		ft_putnbr(fd, n % 10);
	}
}
